namespace Aplicacao
{
    public static class Utilitarios
    {
        public static int Somar(int valor01, int valor02)
            => valor01 + valor02;

        public static int Multiplicar(int valor01, int valor02)
            => valor01 * valor02;

        public static int Subtrair(int valor01, int valor02)
            => valor01 - valor02;

        public static int Dividir(int valor01, int valor02)
            => valor01 / valor02;
    }
}